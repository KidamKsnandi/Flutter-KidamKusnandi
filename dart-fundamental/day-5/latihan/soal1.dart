void main() {
 
  print('-------------------------------------');
  print('LOOPING PERTAMA');
  print('-------------------------------------');

  int pertama = 2;
  while(pertama <= 20) { 
    print(pertama.toString() + " - I love coding");
  pertama += 2;
  }

  print('-------------------------------------');
  print('LOOPING KEDUA');
  print('-------------------------------------');
 
  int kedua = 20;
  while(kedua >= 2) { 
    print(kedua.toString() + " - I will become a mobile developer");
  kedua -= 2;
  }
 
}